const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const marketValidation = require('../../validations/market.validation');
const marketController = require('../../controllers/market.controller');

const router = express.Router();

// /v1/markets
router
  .route('/')
  .post(auth('manageMarkets'), validate(marketValidation.createMarket), marketController.createMarket)
  .get(auth('getMarkets'), validate(marketValidation.getMarkets), marketController.getMarkets);

// /v1/markets/:marketId
// ex: /v1/markets/c584dbe
router
  .route('/:marketId')
  .get(auth('getMarkets'), validate(marketValidation.getMarket), marketController.getMarket)
  .patch(auth('manageMarkets'), validate(marketValidation.updateMarket), marketController.updateMarket)
  .delete(auth('manageMarkets'), validate(marketValidation.deleteMarket), marketController.deleteMarket);

module.exports = router;

